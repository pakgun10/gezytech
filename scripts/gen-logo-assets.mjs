// Regenerates the Hivekeep logo assets from the extracted geometry in
// scripts/logo-paths.json (the 13 sub-shapes of the mark, in 1254x1254 space,
// pulled from the original vectorized logo). Outputs:
//   - src/client/components/common/hivekeep-logo-paths.ts  (data for the React component)
//   - logo.svg                                           (root, baked-gradient master)
//   - site/public/logo.svg                               (Astro <img> copy)
// The dynamic, theme-aware logo is <HivekeepLogo/>; these SVGs are for plain
// <img>/README/OG contexts. Run: bun scripts/gen-logo-assets.mjs
// NOTE: this does NOT regenerate the PWA raster icons (favicon/app/apple/
// maskable) under src/client/public/. After changing the logo, also run
// `bun scripts/gen-pwa-icons.mjs` (it rasterizes from the logo.svg written here).
import { readFileSync, writeFileSync } from 'node:fs'

const { paths } = JSON.parse(
  readFileSync(new URL('./logo-paths.json', import.meta.url), 'utf8'),
)

// Tight, optically-centered square window over the artwork bbox
// (bbox x201.3 y225.8 w850.6 h753.9, center 626.6/602.8). Kept square so the
// component's `size` yields a square mark.
const VIEWBOX = '152 128 950 950'
// Diagonal of the real artwork bbox, for userSpaceOnUse gradients.
const GRAD = { x1: 201, y1: 226, x2: 1052, y2: 980 }

// 1) React component data
writeFileSync(
  new URL('../src/client/components/common/hivekeep-logo-paths.ts', import.meta.url),
  `// AUTO-GENERATED by scripts/gen-logo-assets.mjs. Do not edit by hand.
// The 13 sub-shapes together form the Hivekeep mark (a bee inside a honeycomb
// cluster). Fill them all with one paint to recolor.
/* eslint-disable */

/** Square viewBox tightly centered on the mark. */
export const HIVEKEEP_LOGO_VIEWBOX = '${VIEWBOX}'

/** Diagonal of the real artwork bbox, for userSpaceOnUse gradients. */
export const HIVEKEEP_LOGO_GRADIENT_LINE = ${JSON.stringify(GRAD)}

/** The mark's sub-paths, in the original 1254x1254 coordinate space. */
export const HIVEKEEP_LOGO_PATHS: readonly string[] = [
${paths.map((d) => `  ${JSON.stringify(d)},`).join('\n')}
]
`,
)

// 2) Static baked-gradient SVGs (root master + Astro site copy)
const svg = `<svg xmlns="http://www.w3.org/2000/svg" viewBox="${VIEWBOX}" width="256" height="256" role="img" aria-label="Hivekeep">
  <defs>
    <linearGradient id="hivekeep-aurora" gradientUnits="userSpaceOnUse" x1="${GRAD.x1}" y1="${GRAD.y1}" x2="${GRAD.x2}" y2="${GRAD.y2}">
      <stop stop-color="#AE5AF9"/>
      <stop offset="0.52" stop-color="#FB5FCA"/>
      <stop offset="1" stop-color="#FFB470"/>
    </linearGradient>
  </defs>
  <g fill="url(#hivekeep-aurora)">
${paths.map((d) => `    <path d="${d}"/>`).join('\n')}
  </g>
</svg>
`
for (const rel of ['../logo.svg', '../site/public/logo.svg']) {
  writeFileSync(new URL(rel, import.meta.url), svg)
}

console.log(`regenerated logo assets from ${paths.length} paths`)
